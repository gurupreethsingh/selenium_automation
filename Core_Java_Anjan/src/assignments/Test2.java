package assignments;

public class Test2 {

	public static void main(String[] args) {
		//Even numbers from 1-20
		for (int i=1;i<=20;i++)
		{
			if(i%2==0)
			{
				System.out.println(i);
			}
			
		}
		System.out.println();
		//odd numbers from 1-20
		for (int i=1;i<=20;i++)
		{
			if(i%2!=0)
			{
				System.out.println(i);
			}
			
		}

	}

}
