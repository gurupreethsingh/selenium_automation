package java_basics;

public class Operators {

	public static void main(String[] args) 
	{
		// Arithematic operators ( + , -, * , /,  %)
		int a = 7; 
		int b = 2; 
		float a1 = 7f;
		float b1 = 2f; 
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);   // 3.5 it will give  only 3, 
		System.out.println(a1/b1);   // 3.5
		System.out.println(a%b);
		
		// relational operators   <  >  <=  >=  == !=  ( result in boolean) 
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a >= b);
		
		// logical operaters (and , or, not) 
		// logical and operator. 
	}
}
